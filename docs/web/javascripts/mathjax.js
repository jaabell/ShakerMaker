// MathJax v3 config for pymdownx.arithmatex (generic mode).
// The document$ subscription re-typesets on every page change — required
// because Material's navigation.instant loads pages via SPA, so MathJax
// would otherwise only run once on the first load.
window.MathJax = {
  tex: {
    inlineMath: [["\\(", "\\)"]],
    displayMath: [["\\[", "\\]"]],
    processEscapes: true,
    processEnvironments: true
  },
  options: {
    ignoreHtmlClass: ".*|",
    processHtmlClass: "arithmatex"
  }
};

document$.subscribe(() => {
  MathJax.startup.output.clearCache();
  MathJax.typesetClear();
  MathJax.texReset();
  MathJax.typesetPromise();
});
